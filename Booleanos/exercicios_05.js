/* Exercício 05: Controle de Volume
Se volume for maior que 70 → "Volume alto"
Entre 30 e 70 → "Volume médio"
Abaixo de 30 → "Volume baixo" */
let volume = 10
if(volume > 70){
    console.log("Volume alto")
}else if(volume > 30){
    console.log("Volume médio")
}else{
    console.log("Volume baixo")
}